// Helper function to get profession icon
export function getProfessionIcon(profession: string): string | undefined {
  const key = profession.toLowerCase() as keyof typeof GW2_ICONS;
  return GW2_ICONS[key];
}

// Helper function to get discipline icon
export function getDisciplineIcon(discipline: string): string | undefined {
  const normalized = discipline.toLowerCase().replace(/\s+/g, '');
  const mapping: Record<string, keyof typeof GW2_ICONS> = {
    armorsmith: 'armorsmith',
    weaponsmith: 'weaponsmith',
    huntsman: 'huntsman',
    artificer: 'artificer',
    leatherworker: 'leatherworker',
    tailor: 'tailor',
    jeweler: 'jeweler',
    chef: 'chef',
    cook: 'chef',
    scribe: 'scribe',
  };
  const key = mapping[normalized];
  return key ? GW2_ICONS[key] : undefined;
}

